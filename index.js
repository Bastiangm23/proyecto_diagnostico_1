function votar() {
    var nombre = document.getElementById('nombre').value;
    var alias = document.getElementById('alias').value;
    var rut = document.getElementById('rut').value;
    var email = document.getElementById('email').value;
    var region = document.getElementById('region_select').value;
    var comuna = document.getElementById('comuna_select').value;
    var candidato = document.getElementById('candidato_select').value;
    var web = document.getElementById('web').checked;
    var tv = document.getElementById('tv').checked;
    var rrss = document.getElementById('rrss').checked;
    var amigo = document.getElementById('amigo').checked;
    var valido_para_votar = true;


    if(nombre == ''){
        alert("Debe Ingresar el nombre.");
        valido_para_votar = false;
    }else if(alias == ''){
        alert("Debe Ingresar el alias.");
        valido_para_votar = false;
    }else if(rut == ''){
        alert("Debe Ingresar el rut.");
        valido_para_votar = false;
    }
    else if(!validarRut(rut)) {
        alert("El RUT es inválido.");
        valido_para_votar = false;
    }else if(email == ''){
        alert("Debe Ingresar el email.");
        valido_para_votar = false;
    }
    else if(!validarEmail(email)) {
        alert("El email es inválido.");
        valido_para_votar = false;
    }
    else if(region == 0){
        alert("Debe seleccionar una region.");
        valido_para_votar = false;
    }else if(comuna == 0){
        alert("Debe seleccionar una comuna.");
        valido_para_votar = false;
    }else if(candidato == 0){
        alert("Debe seleccionar un candidato.");
        valido_para_votar = false;
    }

    
    if(valido_para_votar){
        $.ajax({
            url: "guardar_voto.php",
            type: "POST",
            data: {
                nombre: nombre,
                alias: alias,
                rut: rut,
                email: email,
                region: region,
                comuna: comuna,
                candidato: candidato,
                web: web,
                tv: tv,
                rrss: rrss,
                amigo: amigo
            },
            success: function(response){
                if(response == 1){
                    alert("¡Voto registrado correctamente!");
                    location.reload();
                }else{
                    alert("El rut ingresado ya esta en uso, no se pudo registrar el voto");
                }
            },
            error: function(xhr, status, error){
                console.error(xhr.responseText);
                alert("Error al registrar el voto. Por favor, inténtalo de nuevo.");
            }
        });
    }
        
    
}


function validarRut(rut) {
    // Eliminar puntos y guión del rut (si los hay)
    rut = rut.replace(/\./g,'').replace(/\-/g,'');

    // Separar el rut y el dígito verificador
    var rutLimpio = rut.slice(0, -1);
    var digitoVerificador = rut.slice(-1).toUpperCase();

    // Validar que el rut tenga formato válido
    if (!/^[0-9]+[0-9kK]{1}$/.test(rut)) return false;

    // Calcular el dígito verificador esperado
    var suma = 0;
    var multiplo = 2;
    for (var i = rutLimpio.length - 1; i >= 0; i--) {
        suma += parseInt(rutLimpio.charAt(i)) * multiplo;
        if (multiplo < 7) multiplo++;
        else multiplo = 2;
    }
    var resto = suma % 11;
    var dvEsperado = 11 - resto;
    if (dvEsperado == 10) dvEsperado = 'K';
    else if (dvEsperado == 11) dvEsperado = '0';

    // Comparar el dígito verificador esperado con el ingresado
    return dvEsperado == digitoVerificador;
}

function validarEmail(email) {
    // Expresión regular para validar el formato de email
    var regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return regex.test(email);
}


function cargar_regiones(){
    select_region = document.getElementById('region_select');
    regiones.regiones.forEach(region => {
        // Crear un nuevo elemento option
        var nuevaOpcion = document.createElement("option");
        nuevaOpcion.value = region.region;
        nuevaOpcion.text = region.region;

        select_region.appendChild(nuevaOpcion);
    });

}

function actualizar_comunas(){
    select_comuna = document.getElementById('comuna_select');
    region_seleccionada = document.getElementById('region_select').value;

    // limpiamos las comunas
    while (select_comuna.firstChild) {
        select_comuna.removeChild(select_comuna.firstChild);
    }
    
    regiones.regiones.forEach(region => {
        if (region.region == region_seleccionada) {
            var nuevaOpcion = document.createElement("option");
                nuevaOpcion.value = 0;
                nuevaOpcion.text = 'Seleccione una comuna';

                select_comuna.appendChild(nuevaOpcion);
            region.comunas.forEach(comuna => {
                var nuevaOpcion = document.createElement("option");
                nuevaOpcion.value = comuna;
                nuevaOpcion.text = comuna;

                select_comuna.appendChild(nuevaOpcion);

            });
        }
    });



}

